#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t len)
{
	const unsigned char	*s;
	unsigned char		*d;

	if (!dest && !src)
		return (dest);
	if (dest < src)
		return (ft_memcpy(dest, src, len));
	s = (unsigned char *)src;
	d = (unsigned char *)dest;
	while (len--)
		d[len] = s[len];
	return (dest);
}
